#include "../includes/minishell.h"

int	ft_issymbol(char c)
{
	if (c >= '!' && c <= '-')
		return (1);
	if (c >= ';' && c <= '<')
		return (1);
	if (c >= '>' && c <= '@')
		return (1);
	if (c >= '[' && c <= '^')
		return (1);
	if (c >= '{' && c <= '~')
		return (1);
	return (0);
}
